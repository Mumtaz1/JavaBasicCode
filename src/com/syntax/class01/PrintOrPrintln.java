package com.syntax.class01;

public class PrintOrPrintln {

	public static void main(String[] args) {
		// I want to see the difference between println vs. print
		// print - prints all on one line
		System.out.print("Hello");
		
		System.out.print("How are you?");
		
		System.out.print("I am learning coding");
		
		// println - prints one per line
		System.out.println("Hello");
		
		System.out.println("How are you?");
		
		System.out.println("I am learning coding");
		
		// Combination of print and println. It will print HelloHow are you on one line and then I am learning coding on next line.
		System.out.print("Hello");
		
		System.out.println("How are you?");
		
		System.out.println("I am learning coding");
		
		// Combination of print and println. This time though it will print everything on one line.
		System.out.print("Hello");
		
		System.out.print("How are you?");
		
		System.out.println("I am learning coding");

	}

}
