package com.syntax.class03;

public class DoubleComparison {
	
	public static void main(String[] args) {
		
		/* Create a Java program and name it Double Comparison.
		 * Declare 2 double values and if value of first variable is higher than the 
		 * second, print "Double value _ is larger than _." Otherwise print
		 */
		
		double a = 10.55;
		double b = 5.46;
		if (a > b) {
			System.out.println("Double value  " + a + " is larger than " + b); 
		}	else {
			System.out.println("Double value  " + b + " is smaller than " + a);	
		}
	}

}
